function convertText(text, styleId) {
  const map = unicodeMaps[styleId];
  
  if (!map) {
    return text;
  }

  // Standard character mapping conversion
  return text
    .split('')
    .map(char => map[char] || char)
    .join('');
}

function getAllConversions(text) {
  const conversions = {};
  
  Object.keys(unicodeMaps).forEach(styleId => {
    conversions[styleId] = convertText(text, styleId);
  });

  return conversions;
}

function copyToClipboard(text) {
  if (navigator.clipboard && window.isSecureContext) {
    return navigator.clipboard.writeText(text).then(() => true).catch(() => false);
  } else {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = text;
    textArea.style.position = 'fixed';
    textArea.style.left = '-999999px';
    textArea.style.top = '-999999px';
    document.body.appendChild(textArea);
    textArea.focus();
    textArea.select();
    
    try {
      const result = document.execCommand('copy');
      document.body.removeChild(textArea);
      return Promise.resolve(result);
    } catch (err) {
      document.body.removeChild(textArea);
      return Promise.resolve(false);
    }
  }
}

